#!/bin/bash
###############################################################################
# AUTOPILOT_LOOP.sh
# 하니스(Harness) 기반 랄프 위검 루프(Ralph Wiggum Loop) 메인 실행 스크립트
#
# AI 에이전트의 컨텍스트 윈도우 한계를 극복하기 위해:
# 1) 외부 기억 장치(context/) + 훅(hooks/) + 스킬(skills/) 문서를 조합하여
#    "이번 루프에서 AI가 읽을 완벽한 프롬프트"를 자동 생성한다.
# 2) AI가 작업을 완료하면 기억(대화)을 완전히 초기화하고
#    업데이트된 문서를 기반으로 다음 프롬프트를 재생성하여 무한 반복한다.
#
# 사용법:
#   bash AUTOPILOT_LOOP.sh
#   (또는 Git Bash, WSL, macOS Terminal에서 실행)
###############################################################################

# === 설정 ===
PIPLINE_KIT_DIR="$(cd "$(dirname "$0")" && pwd)"
CONTEXT_DIR="$PIPLINE_KIT_DIR/context"
HOOKS_DIR="$PIPLINE_KIT_DIR/hooks"
SKILLS_DIR="$PIPLINE_KIT_DIR/skills"
SYSTEM_PROMPT="$PIPLINE_KIT_DIR/0_AGENT_SYSTEM_PROMPT.md"
TEMP_PROMPT="$PIPLINE_KIT_DIR/.temp_prompt.txt"

LOOP_COUNT=0

echo "╔══════════════════════════════════════════════════════════════╗"
echo "║  🚀 하니스 오토파일럿 (Ralph Wiggum Loop) 시작              ║"
echo "║  AI 컨텍스트 초기화 + 외부 기억 장치 기반 무한 자동화       ║"
echo "╚══════════════════════════════════════════════════════════════╝"
echo ""
echo "📁 pipline-kit 경로: $PIPLINE_KIT_DIR"
echo ""

# === 무한 반복 랄프 루프 ===
while true; do
  LOOP_COUNT=$((LOOP_COUNT + 1))
  echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
  echo "🔄 [Loop #$LOOP_COUNT] 외부 기억 장치와 훅(Hook)을 로드합니다."
  echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

  # ---------------------------------------------------------------
  # 1단계: 시스템 프롬프트 로드
  # ---------------------------------------------------------------
  AGENT_SYSTEM=""
  if [ -f "$SYSTEM_PROMPT" ]; then
    AGENT_SYSTEM=$(cat "$SYSTEM_PROMPT")
  fi

  # ---------------------------------------------------------------
  # 2단계: 시작 전 훅(Pre-Hook) 로드
  # ---------------------------------------------------------------
  PRE_HOOK=""
  if [ -f "$HOOKS_DIR/pre_hook.md" ]; then
    PRE_HOOK=$(cat "$HOOKS_DIR/pre_hook.md")
  fi
  DOMAIN_HOOK=""
  if [ -f "$HOOKS_DIR/pre_run_domain_check.md" ]; then
    DOMAIN_HOOK=$(cat "$HOOKS_DIR/pre_run_domain_check.md")
  fi

  # ---------------------------------------------------------------
  # 3단계: 외부 기억 장치(Context) 로드
  # ---------------------------------------------------------------
  PLAN=""
  if [ -f "$CONTEXT_DIR/1_plan.md" ]; then
    PLAN=$(cat "$CONTEXT_DIR/1_plan.md")
  fi

  CONTEXT_NOTE=""
  if [ -f "$CONTEXT_DIR/2_context_note.md" ]; then
    CONTEXT_NOTE=$(cat "$CONTEXT_DIR/2_context_note.md")
  fi

  CHECKLIST=""
  if [ -f "$CONTEXT_DIR/3_checklist.md" ]; then
    CHECKLIST=$(cat "$CONTEXT_DIR/3_checklist.md")
  fi

  # ---------------------------------------------------------------
  # 4단계: 완료 후 훅(Post-Hook) 로드
  # ---------------------------------------------------------------
  POST_HOOK=""
  if [ -f "$HOOKS_DIR/post_hook.md" ]; then
    POST_HOOK=$(cat "$HOOKS_DIR/post_hook.md")
  fi

  # ---------------------------------------------------------------
  # 5단계: 통합 프롬프트 생성
  # ---------------------------------------------------------------
  cat <<PROMPT_EOF > "$TEMP_PROMPT"
============================================================
[시스템 프롬프트 - 너의 정체와 행동 원칙]
============================================================
$AGENT_SYSTEM

============================================================
[시작 전 강제 훅 - 작업 전 반드시 숙지]
============================================================
$DOMAIN_HOOK

$PRE_HOOK

============================================================
[외부 기억 장치 1: 전체 계획 — context/1_plan.md]
============================================================
$PLAN

============================================================
[외부 기억 장치 2: 현재 맥락 및 변수 노트 — context/2_context_note.md]
============================================================
$CONTEXT_NOTE

============================================================
[외부 기억 장치 3: 할 일 체크리스트 — context/3_checklist.md]
============================================================
$CHECKLIST

============================================================
[이번 루프 목표]
============================================================
1) 위의 체크리스트(3_checklist.md)에서 아직 [ ]로 표시된 가장 위의 항목 하나를 찾아 이번 루프의 목표로 삼아라.
2) 해당 항목 옆에 적힌 스킬 파일(skills/*.md)을 읽고, 그 매뉴얼의 지침에 따라 작업을 수행해라.
3) 도메인 룰(skills/domain_rules.md)이 존재하면 반드시 해당 규칙에 맞게 코딩해라.
4) 작업 중 범위 외 기능이 발견되면 절대 구현하지 말고 BACKLOG에 기록만 해라.

============================================================
[완료 후 강제 훅 - 이 체크를 모두 마쳐야 루프 종료 가능]
============================================================
$POST_HOOK

============================================================
[최종 지시]
============================================================
위의 모든 훅과 규칙을 지키며 태스크를 완수하고,
external 기억 장치(context/ 폴더의 3개 파일)를 모두 업데이트한 후,
"LOOP_DONE" 이라고 선언해.
PROMPT_EOF

  echo ""
  echo "✅ 통합 프롬프트가 생성되었습니다: $TEMP_PROMPT"
  echo "   ($(wc -l < "$TEMP_PROMPT" 2>/dev/null || echo "?") 라인)"
  echo ""

  # ---------------------------------------------------------------
  # AI CLI 자동 연동 포인트
  # 아래 주석을 해제하면 사람 개입 없이 100% 자동화 가능!
  # ---------------------------------------------------------------
  # 예시 1: Aider 사용 시
  # aider --message-file "$TEMP_PROMPT"
  #
  # 예시 2: Claude Code (Anthropic CLI) 사용 시
  # claude --print "$TEMP_PROMPT"
  #
  # 예시 3: Gemini CLI 사용 시
  # gemini < "$TEMP_PROMPT"
  #
  # 예시 4: OpenAI Codex CLI 사용 시
  # codex --prompt-file "$TEMP_PROMPT"
  # ---------------------------------------------------------------

  echo "╔══════════════════════════════════════════════════════════════╗"
  echo "║  📋 사용 방법                                              ║"
  echo "║                                                            ║"
  echo "║  1. .temp_prompt.txt 파일의 내용을 복사하세요              ║"
  echo "║  2. 클로드/제미나이/Cursor IDE 채팅창에 붙여넣으세요       ║"
  echo "║  3. AI가 작업을 마치고 LOOP_DONE을 선언하면                ║"
  echo "║  4. 이 터미널로 돌아와 엔터를 누르세요                     ║"
  echo "║                                                            ║"
  echo "║  (루프 종료: Ctrl+C)                                       ║"
  echo "╚══════════════════════════════════════════════════════════════╝"
  echo ""

  read -p "⏳ AI 작업 완료 후 엔터를 누르면 다음 루프를 시작합니다... "

  echo ""
  echo "🧹 이전 기억을 초기화하고 맑은 정신으로 다음 루프를 준비합니다..."
  echo ""
  sleep 1
done
